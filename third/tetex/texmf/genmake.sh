#!/bin/sh
# $Id: genmake.sh,v 1.3 2003-02-26 22:49:33 ghudson Exp $

# genmake.sh - Shell script to generate Makefile in this directory.

# Direct output to Makefile
exec >Makefile

echo '# $Id: genmake.sh,v 1.3 2003-02-26 22:49:33 ghudson Exp $'
echo "# Generated by genmake.sh - do not edit!"
echo "# Symlinks are installed by ../Makefile.athena"
echo ""
echo "TEXMFDIR=/usr/athena/share/texmf"
echo ""
echo "install:"

find . -type d ! -name CVS ! -name . -print |
	sed -e 's,^./\(.*\)$,	mkdir -p ${DESTDIR}${TEXMFDIR}/\1,'

find . -type d -name CVS -prune -o \
	-type f ! -name genmake.sh ! -name Makefile -print |
	sed -e 's,^./\(.*\)$,	cp \1 ${DESTDIR}${TEXMFDIR}/\1,'
